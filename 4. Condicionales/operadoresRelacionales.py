
a = int(input("Ingresa el primer numero: "))
b = int(input("Ingresa el segundo numero: "))

#Igual de (==)
if a == b:
    print("a y b son iguales")
else:
    print("a y b son diferentes")

#Distinto de (!=)
if a != b:
    print("a y b son diferentes")
else:
    print("a y b son iguales")

#Mayor que (>)
if a == b:
    print("a y b son iguales")
elif a > b:
    print("a es mayor de que b")
else:
    print("a no es mayor que b")